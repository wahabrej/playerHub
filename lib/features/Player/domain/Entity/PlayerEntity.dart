class PlayerEntity {
  final String Id;
  final String name;
  final String parent_email;
  final String parent_phone;
  final String username;
  final String parent_name;
  final String avatar;

  PlayerEntity({
    required this.Id,
    required this.name,
    required this.parent_email,
    required this.parent_phone,
    required this.username,
    required this.parent_name,
    required this.avatar,
  });
}
